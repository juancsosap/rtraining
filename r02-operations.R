# Arithmetics Operators
10 + 3 # 13
10 - 3 # 7
10 * 3 # 30
10 / 3 # 3.333333
10 ^ 3 # 1000
10 %/% 3 # 3
10 %% 3 # 1

10 + 3 * 5 # 25
(10 + 3) * 5 # 65

# Relational Operators
10 > 5             # TRUE
10 < -10.5         # FALSE
"ABCD" >= "abcd"   # TRUE
"abcf" <= "1234"   # FALSE
100 == 100.0       # TRUE
100 != "100"       # FALSE

# Logical Operators
TRUE & TRUE     # TRUE
TRUE & FALSE    # FALSE
FALSE & TRUE    # FALSE
FALSE & FALSE   # FALSE

TRUE | TRUE     # TRUE
TRUE | FALSE    # TRUE
FALSE | TRUE    # TRUE
FALSE | FALSE   # FALSE

!TRUE           # FALSE
!FALSE          # TRUE
